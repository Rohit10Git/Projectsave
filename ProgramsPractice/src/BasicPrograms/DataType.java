package BasicPrograms;

public class DataType 
{
	public static void main(String [] args)             //Main method
	{
		boolean a= true;                               //Boolean Data type
		System.out.println("boolean : " +a);
		
		char name= 'A';                                //Char Data type
		System.out.println("char : " +name);		
		
		byte b= 20;                                     //Byte Data type
		System.out.println("byte : " +b);
		
		int c= 15426;                                   //Int Data type
		System.out.println("int: " +c);
		
		short d= 660;                                   //Short Data type
		System.out.println("short : " +d);
		
		long e= -156243L;                               //Long Data type
		System.out.println("long : " +e);
		
		float f= 120.50f;                               //Float Data type
		System.out.println("float : " +f);
		
		double g= 22.22;                                //Double Data type
		System.out.println("double : " +g);
	}       
}
