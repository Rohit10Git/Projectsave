package BasicPrograms;

public class SwapTwoNumbers 
{
	public static void main(String[] args) 
	{
		int H= 88;
		int L= 44;
		
		int T = H;                                         // T = Temporary Variable
		      H = L;
		      L = T;
		    
		    System.out.println("The value of H becomes = " +H);               // Print Statement
		    System.out.println("The value of L becomes = " +L);              // Print Statement 
	}
}
