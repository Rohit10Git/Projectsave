package BasicPrograms;

public class EvenOrOddNumber
{
	public static void main(String args[])                    // Main Method
	{
		int N= 200;
		
		if (N %2 == 0)                                                       // If Condition
		{
			System.out.println(200+ " Is Even Number.");       // If Condition =True; Result Will Be Even number
		}
		
		else                                                               // Else Condition
		{
			System.out.println(200+ " Is Odd Number.");      // If Condition =False; Result Will Be Odd number
		}
	}
}
