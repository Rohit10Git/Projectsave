package BasicPrograms;

public class TableOfNumber 
{
	public static void main(String args[])             // Main Method
	{
			int N= 8;
			int result;	
			
			for(int i=1;i<=10;i++)                       // For loop
			{
				result= N*i;
				System.out.println(N+"*"+i+" = " +result); 			
			}
	}
}

